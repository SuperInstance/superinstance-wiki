if __name__ == "__main__":
    print("Eisenstein Snap — Sanity Checks")
    print("=" * 40)

    # Basic arithmetic
    e1 = EisensteinInteger(1, 1)   # 1 + ω
    e2 = EisensteinInteger(2, 3)   # 2 + 3ω
    e_sum = e1 + e2
    print(f"(1+ω) + (2+3ω) = {e_sum}  (expected: 3 + 4ω)")
    assert e_sum.a == 3 and e_sum.b == 4

    e_prod = e1 * e2
    # (1+ω)(2+3ω) = 2 + 3ω + 2ω + 3ω² = 2 + 5ω + 3(-1-ω) = 2 + 5ω - 3 - 3ω = -1 + 2ω
    print(f"(1+ω)(2+3ω) = {e_prod}  (expected: -1 + 2ω)")
    assert e_prod.a == -1 and e_prod.b == 2

    # Norm check
    e = EisensteinInteger(2, -1)
    print(f"N({e}) = {e.norm()}  (expected: 2² - 2(-1) + (-1)² = 4+2+1 = 7)")
    assert e.norm() == 7

    # Units
    units = [EisensteinInteger(1, 0), EisensteinInteger(-1, 0),
             EisensteinInteger(0, 1), EisensteinInteger(0, -1),
             EisensteinInteger(1, 1), EisensteinInteger(-1, -1)]
    print(f"Six units: {[str(u) for u in units]}")
    assert all(u.is_unit() for u in units)

    # Lattice snap
    w = np.array([0.015, -0.008, 1.0, 0.0], dtype=np.float32)
    snapped = snap_to_lattice(w, scale=0.01)
    print(f"Original: {w}")
    print(f"Snapped (scale=0.01): {snapped}")

    # Mutation shape
    mutated = eisenstein_mutation(w, mutation_rate=0.5, scale=0.01, seed=42)
    print(f"Mutated (rate=0.5): {mutated}")
    assert mutated.shape == w.shape

    # Roundtrip
    ok = test_eisenstein_roundtrip()
    print(f"Roundtrip idempotent: {ok}")
    assert ok

    print("\nAll sanity checks passed ✓")
